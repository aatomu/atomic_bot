	for _, guild := range discord.State.Guilds {
		for _, vs := range guild.VoiceStates {
			if v.ChannelID == vs.ChannelID && vs.UserID != clientID {
				return
			}
			tmp = tmp + vs.UserID + ","
		}
	}
